def normalize_hex(hex_value: str) -> str:
    """
    Normalize a hexadecimal color value to 6 digits, lowercase.
    """
    match = HEX_COLOR_RE.match(hex_value)
    if match is None:
        raise ValueError(f"'{hex_value}' is not a valid hexadecimal color value.")
    hex_digits = match.group(1)
    if len(hex_digits) == 3:
        hex_digits = "".join(2 * s for s in hex_digits)
    return f"#{hex_digits.lower()}"


def name_to_hex(name: str) -> str:
    """
    Returns a hexadecimal color value for a CSS3 color name.
    """
    hex_value = CSS3_NAMES_TO_HEX.get(name.lower())
    if hex_value is None:
        raise ValueError("Not a valid CSS3 color name")
    return hex_value


def hex_to_rgba(hex_value: str) -> RGBA:
    """
    Convert a hexadecimal color value to a 3-tuple of integers
    suitable for use in an ``rgba()`` specifying that color.
    """
    int_value = int(normalize_hex(hex_value)[1:], 16)
    return RGBA(int_value >> 16, int_value >> 8 & 0xFF, int_value & 0xFF, 1)


def parse_rgba(rgb_or_rgba) -> RGBA:
    match = RGBA_COLOR_RE.match(rgb_or_rgba.replace(" ", ""))
    if match is None:
        raise ValueError("Cannot parse RGB(A)")
    match = tuple(
        int(i) if i.isdigit() else float(i) for i in match.groups() if i is not None
    )
    rgba = RGBA(*match)
    if rgba.alpha > 1:
        raise ValueError("Alpha value too big")
    if rgba.red > 255 or rgba.green > 255 or rgba.blue > 255:
        raise ValueError("RGB values out of range")
    return rgba


def parse(value: str) -> RGBA:
    """
    Attempts to parse any color input to a ``rgba()`` value.
    """
    # Normalize a CSS3 color name to hex
    try:
        value = name_to_hex(value)
    except ValueError:
        pass
    # It might be a hex value
    try:
        return hex_to_rgba(value)
    except ValueError:
        pass
    # Else, try parsing rgba and allow errors
    return parse_rgba(value)
